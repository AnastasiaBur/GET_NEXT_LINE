#include "libft.h"

int		ft_sqrt(int nb)
{
	int s;

	s = 1;
	if (nb <= 0)
		return (0);
	while (s < 46341)
	{
		if (s * s == nb)
			return (s);
		s++;
	}
	return (0);
}
